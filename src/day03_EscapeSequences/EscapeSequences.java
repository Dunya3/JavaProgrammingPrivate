package day03_EscapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#");
        System.out.println("Hello Cydeo \nHow are you today? \nIt's nice to see you all! \nWhat class do we have next week?");
        System.out.println("-----------------------------------");
        System.out.println("\tJava is Coll Programming Lenguage");
        System.out.println("-----------------------------------");
        System.out.println("/ \\");// in order to print one backward slash we need to give two backward slashes
        System.out.println("-----------------------------------");
        System.out.println("My favorite TV show is \"Game of Trones\"");// in order to print double quote we need to use \"




    }
}
